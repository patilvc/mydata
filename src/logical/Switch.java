package logical;

public class Switch {

	public static void main(String[] args) {
		int input =3;
		
		switch (input) {
		
		case 1:
			System.out.println( "monday ");
			break;
		case 2:
			System.out.println( "saturaday");
			break;
		case 3:
			System.out.println( " Tueday");
			break;
		case 4:
			System.out.println( "friday");
			break;
		default:
			System.out.println( "anyother day ");
			break;
		
		
		}

	}

}
